public class Calculohipotenusa {
    //Ejercicio para calcular la hipotenusa de un triángulo rectángulo a partir de la longitud de sus otros lados  o catetos
    public static void main(String[] args) {
        double lado1 = 3;
        double lado2 = 4;
        double operacionHipotenusa = Math.sqrt((lado1 * lado1) + (lado2 * lado2));
        System.out.println("Hipotenusa: " + operacionHipotenusa);
    }
}


